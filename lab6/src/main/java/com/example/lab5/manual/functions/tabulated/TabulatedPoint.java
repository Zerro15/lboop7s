package com.example.lab5.manual.functions.tabulated;

/**
 * Точка табулированной функции.
 */
public class TabulatedPoint {
    private final double x;
    private final double y;

    public TabulatedPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
